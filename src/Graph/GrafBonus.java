    private void cmbxTitikAwalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbxTitikAwalActionPerformed
        int start = cmbxTitikAwal.getSelectedIndex();
        int end = cmbxTitikAkhir.getSelectedIndex();
        
        if (start == end) {
            txtInputJarak.setEditable(false);
            btnTambahSimpul.setEnabled(false);
            txtInputJarak.setText("JARAK TIDAK ADA");
        } else if (Graph.adjM.get(start).get(end) != 1000000000){
            txtInputJarak.setEditable(true);
            txtInputJarak.setText(Integer.toString(Graph.adjM.get(start).get(end)));
        } else {
            txtInputJarak.setEditable(true);
            txtInputJarak.setText("0");
        }
    }//GEN-LAST:event_cmbxTitikAwalActionPerformed

