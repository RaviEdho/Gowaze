    private void txtSimpulBaruKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSimpulBaruKeyReleased
        if("".equals(txtSimpulBaru.getText()) || "Masukkan nama simpul...".equals(txtSimpulBaru.getText()) || !GUI.ceknamasimpul(txtSimpulBaru.getText())) {
            areaInfoSimpul.setText("");
            if (!GUI.ceknamasimpul(txtSimpulBaru.getText())) {
                areaInfoSimpul.setText("Nama simpul tidak boleh sama dengan simpul yang ada!");
            }
            btnTambahSimpul.setEnabled(false);
        } else {
            areaInfoSimpul.setText("");
            btnTambahSimpul.setEnabled(true);
        } 
    }//GEN-LAST:event_txtSimpulBaruKeyReleased

