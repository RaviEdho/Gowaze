    private void cmbxLihatSimpulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbxLihatSimpulActionPerformed
        areaInfoSimpul.setText("");
        int idx = cmbxLihatSimpul.getSelectedIndex();
        
        for(int i = 0; i < Graph.jumlahSimpul; i++) {
            if (Graph.adjM.get(idx).get(i) != null && Graph.adjM.get(idx).get(i) != 1000000000) {
                areaInfoSimpul.append("Simpul " + Graph.simpul.get(idx).nama + " terhubung dengan simpul " + Graph.simpul.get(i).nama + " dengan jarak " + Graph.adjM.get(idx).get(i) + "\n");
            }
        }
        if ("".equals(areaInfoSimpul.getText())) {
            areaInfoSimpul.setText("Simpul " + Graph.simpul.get(idx).nama + " tidak memiliki sisi dengan simpul apapun!");
        }
    }//GEN-LAST:event_cmbxLihatSimpulActionPerformed

    private void btnTambahSisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahSisiActionPerformed
        int start = cmbxTitikAwal.getSelectedIndex();
        int end = cmbxTitikAkhir.getSelectedIndex();
        int jrk = Integer.parseInt(txtInputJarak.getText());
        boolean duaarah = ToggleDuaArah.isSelected();
        
        txtInputJarak.setText("");
        if (start != -1 || end != -1) {
            Graph.tambahSisi(0, start, end, jrk, duaarah);
        }
        btnTambahSisi.setEnabled(false);
    }//GEN-LAST:event_btnTambahSisiActionPerformed

