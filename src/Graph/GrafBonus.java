    private void cmbxStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbxStartActionPerformed
        int start = cmbxStart.getSelectedIndex();
        int end = cmbxEnd.getSelectedIndex();
        
        areaInfoJarak.setText("");
        if (start == end) {
            areaInfoJarak.setText("Tidak dapat mencari jarak simpul ke dirinya sendiri");
        } else {
            Graph.carijarak(start);
            Graph.carijalan(start, end);
            if (Graph.jarakfinal != 1000000000) {
                areaInfoJarak.append("Jarak dari simpul " + Graph.simpul.get(start).nama + " dan simpul " + Graph.simpul.get(end).nama + " adalah " + Graph.jarakfinal + "\n");
                areaInfoJarak.append("Jalur yang dilewati: ");
                for (int n : Graph.jalur) {
                    if (n != start) {
                        areaInfoJarak.append(" - ");
                    }
                    areaInfoJarak.append(Graph.simpul.get(n).nama);
                }
            } else {
                areaInfoJarak.setText("Simpul " + Graph.simpul.get(end).nama + " tidak dapat dicapai dari simpul " + Graph.simpul.get(start).nama);
            }
        }
    }//GEN-LAST:event_cmbxStartActionPerformed

