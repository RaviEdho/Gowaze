    private void btnMoveBonusMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMoveBonusMousePressed
        xMouse = evt.getX();
        yMouse = evt.getY();
        GUI.DropButton(btnMoveBonus, 735);
        btnMoveBonus.setBackground(GUI.ButtonBgClicked());
        btnMoveBonus.setCursor(new Cursor(MOVE_CURSOR));
    }//GEN-LAST:event_btnMoveBonusMousePressed

